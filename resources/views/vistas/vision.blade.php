<h4>PROPOSITO</h4>
<p>La ASUSS es una institución técnica, operativa descentralizada que fiscaliza y controla el subsistema de la Seguridad Social de Corto Plazo con la finalidad de mejorar los servicios de salud en beneficio de los asegurados y sus beneficiarios.</p>
<p>En tanto la ASUSS aspira a ser y sobre sus expectativas para el futuro que pretende alcanzar; significa el reto de la institución para cumplir su Misión. Representa la situación deseada y factible con la que nos identificamos en el futuro de nuestra entidad.</p>
